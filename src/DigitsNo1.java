import java.util.Scanner;

class DigitsNo1 {
	public static void main(String[] args) {
		Scanner stdIn = new Scanner(System.in);
		System.out.print("俱计取G");
		int n = stdIn.nextInt();
		if (n == 0) // 0
			System.out.println("赣痊0C");
		else if (n >= -9 && n <= 9) // 应旒
			System.out.println("赣痊碍应旒啤C");
		else
			// G旒匹HW
			System.out.println("赣痊挨G旒匹HWC");
	}
}
